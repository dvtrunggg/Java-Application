package entity;

public class PhongTro {
	private int idPhongTro;
	private String tenPhong;
	private int dienTiech;
	private String moTa;
	private int giaPhong;
	private int tinhTrang;
	public int getIdPhongTro() {
		return idPhongTro;
	}
	public void setIdPhongTro(int idPhongTro) {
		this.idPhongTro = idPhongTro;
	}
	public String getTenPhong() {
		return tenPhong;
	}
	public void setTenPhong(String tenPhong) {
		this.tenPhong = tenPhong;
	}
	public int getDienTiech() {
		return dienTiech;
	}
	public void setDienTiech(int dienTiech) {
		this.dienTiech = dienTiech;
	}
	public String getMoTa() {
		return moTa;
	}
	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}
	public int getGiaPhong() {
		return giaPhong;
	}
	public void setGiaPhong(int giaPhong) {
		this.giaPhong = giaPhong;
	}
	public int getTinhTrang() {
		return tinhTrang;
	}
	public void setTinhTrang(int tinhTrang) {
		this.tinhTrang = tinhTrang;
	}
	
	
}
